uint8_t gf_mul(uint8_t a, uint8_t b) {
    uint8_t result = 0;
    uint8_t hi_bit_set;
    for (int i = 0; i < 8; i++) {
        if (b & 1) {
            result ^= a;
        }
        hi_bit_set = a & 0x80;
        a <<= 1;
        if (hi_bit_set) {
            a ^= 0x1B; // 不可约多项式 x^8 + x^4 + x^3 + x + 1
        }
        b >>= 1;
    }
    return result;
}

void MixColumns(uint8_t *state) {
    uint8_t temp[16];
    for(int i=0; i<16; i++) temp[i] = state[i];
    
    for(int c=0; c<4; c++) {
        int col = c*4;
        state[col] = gf_mul(0x02, temp[col]) ^ gf_mul(0x03, temp[col+1]) ^ temp[col+2] ^ temp[col+3];
        state[col+1] = temp[col] ^ gf_mul(0x02, temp[col+1]) ^ gf_mul(0x03, temp[col+2]) ^ temp[col+3];
        state[col+2] = temp[col] ^ temp[col+1] ^ gf_mul(0x02, temp[col+2]) ^ gf_mul(0x03, temp[col+3]);
        state[col+3] = gf_mul(0x03, temp[col]) ^ temp[col+1] ^ temp[col+2] ^ gf_mul(0x02, temp[col+3]);
    }
}

void InvMixColumns(uint8_t *state) {
    uint8_t temp[16];
    for(int i=0; i<16; i++) temp[i] = state[i];
    
    for(int c=0; c<4; c++) {
        int col = c*4;
        state[col] = gf_mul(0x0e, temp[col]) ^ gf_mul(0x0b, temp[col+1]) ^ gf_mul(0x0d, temp[col+2]) ^ gf_mul(0x09, temp[col+3]);
        state[col+1] = gf_mul(0x09, temp[col]) ^ gf_mul(0x0e, temp[col+1]) ^ gf_mul(0x0b, temp[col+2]) ^ gf_mul(0x0d, temp[col+3]);
        state[col+2] = gf_mul(0x0d, temp[col]) ^ gf_mul(0x09, temp[col+1]) ^ gf_mul(0x0e, temp[col+2]) ^ gf_mul(0x0b, temp[col+3]);
        state[col+3] = gf_mul(0x0b, temp[col]) ^ gf_mul(0x0d, temp[col+1]) ^ gf_mul(0x09, temp[col+2]) ^ gf_mul(0x0e, temp[col+3]);
    }
}

void AddRoundKey(uint8_t *state, uint8_t *round_key) {
    for(int i=0; i<16; i++) {
        state[i] ^= round_key[i];
    }
}

void KeyExpansion(uint8_t *key, uint8_t round_keys[11][16]) {
    uint8_t temp[4];
    
    // 初始轮密钥
    for(int i=0; i<16; i++) {
        round_keys[0][i] = key[i];
    }
    
    // 生成其余轮密钥
    for(int round=1; round<11; round++) {
        // 取前一轮的最后一列
        temp[0] = round_keys[round-1][12];
        temp[1] = round_keys[round-1][13];
        temp[2] = round_keys[round-1][14];
        temp[3] = round_keys[round-1][15];
        
        // RotWord
        uint8_t t = temp[0];
        temp[0] = temp[1];
        temp[1] = temp[2];
        temp[2] = temp[3];
        temp[3] = t;
        
        // SubWord
        temp[0] = S[temp[0]];
        temp[1] = S[temp[1]];
        temp[2] = S[temp[2]];
        temp[3] = S[temp[3]];
        
        // XOR with RCON
        temp[0] ^= RCON[round-1];
        
        // 生成新轮密钥
        for(int i=0; i<4; i++) {
            round_keys[round][i] = round_keys[round-1][i] ^ temp[i];
        }
        for(int i=4; i<16; i++) {
            round_keys[round][i] = round_keys[round-1][i] ^ round_keys[round][i-4];
        }
    }
}

void AES_Encrypt(uint8_t *plaintext, uint8_t *key, uint8_t *ciphertext) {
    uint8_t state[16];
    uint8_t round_keys[11][16];
    
    // 复制明文到状态
    for(int i=0; i<16; i++) state[i] = plaintext[i];
    
    // 密钥扩展
    KeyExpansion(key, round_keys);
    
    // 初始轮密钥加
    AddRoundKey(state, round_keys[0]);
    
    // 9轮
    for(int round=1; round<10; round++) {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, round_keys[round]);
    }
    
    // 最后一轮
    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, round_keys[10]);
    
    // 复制到密文
    for(int i=0; i<16; i++) ciphertext[i] = state[i];
}

void AES_Decrypt(uint8_t *ciphertext, uint8_t *key, uint8_t *plaintext) {
    uint8_t state[16];
    uint8_t round_keys[11][16];
    
    // 复制密文到状态
    for(int i=0; i<16; i++) state[i] = ciphertext[i];
    
    // 密钥扩展
    KeyExpansion(key, round_keys);
    
    // 初始轮密钥加 (最后一轮密钥)
    AddRoundKey(state, round_keys[10]);
    
    // 9轮逆操作
    for(int round=9; round>0; round--) {
        InvShiftRows(state);
        InvSubBytes(state);
        AddRoundKey(state, round_keys[round]);
        InvMixColumns(state);
    }
    
    // 最后一轮
    InvShiftRows(state);
    InvSubBytes(state);
    AddRoundKey(state, round_keys[0]);
    
    // 复制到明文
    for(int i=0; i<16; i++) plaintext[i] = state[i];
}

