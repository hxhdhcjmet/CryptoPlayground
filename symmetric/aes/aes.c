void SubBytes(uint8_t *state){
    /*过S盒
    state: 16字节状态
    */
   size_t i = 0;
   for(i=0;i<16;i++){
        state[i] = S[state[i]];
    }
}

void InvSubBytes(uint8_t *state){
    size_t i = 0;
    for(i=0;i<16;i++){
        state[i] = S_inv[state[i]];
    }
}

void ShiftRows(uint8_t *state){
    //行移位
    //第i行左移i位
    uint8_t temp[16];
    // 复制state到temp
    for(int i=0; i<16; i++) temp[i] = state[i];
    
    // 第0行不变
    state[0] = temp[0];
    state[4] = temp[4];
    state[8] = temp[8];
    state[12] = temp[12];
    
    // 第1行左移1
    state[1] = temp[5];
    state[5] = temp[9];
    state[9] = temp[13];
    state[13] = temp[1];
    
    // 第2行左移2
    state[2] = temp[10];
    state[6] = temp[14];
    state[10] = temp[2];
    state[14] = temp[6];
    
    // 第3行左移3
    state[3] = temp[15];
    state[7] = temp[3];
    state[11] = temp[7];
    state[15] = temp[11];
}

